package uebung2;

public class Schleifen {
    public static void main(String[] args) {
        for(int i = 0; i < 10; i++) {
            System.out.println("Forschleife: " + i);
        }

        System.out.println();

        int i = 0;
        while (i < 10) {
            System.out.println("Whileschleife: " + i);
            i++;
        }
    }
}
